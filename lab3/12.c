#include<stdio.h>
int main(){
    int a;
    scanf("%d",&a);
    switch (a){
    case 1:
        printf("one");
        break;
    case 2:
        printf("Two");
        break;
    case 3:
        printf("Three");
        break;
    case 4:
        printf("Four");
        break;
    case 5:
        printf("Five");
        break;
    case 6:
        printf("six");
        break;
    case 7:
        printf("Seven");
        break;
    case 8:
        printf("eight");
        break;
    case 9:
        printf("nine");
        break;
    case 0:
        printf("zero");
        break;
    default:
        printf("None");
        break;
           }
    return 0;
}
